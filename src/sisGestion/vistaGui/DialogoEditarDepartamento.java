    private static final java.util.logging.Logger logger = java.util.logging.Logger.getLogger(DialogoEditarDepartamento.class.getName());
    private final DepartmentController departmentController;
    private final Department departamentoAEditar;
    private JTextField txtNuevoNombre;

    public DialogoEditarDepartamento(JFrame parent, boolean modal, DepartmentController controller, Department depto) {
        super(parent, modal);
        this.departmentController = controller;
        this.departamentoAEditar = depto;

        setTitle("Editar Departamento");
        setSize(400, 160);
        setLayout(new BorderLayout());
        setLocationRelativeTo(parent);

        
        JPanel formPanel = new JPanel(new FlowLayout(FlowLayout.LEFT, 10, 20));
        formPanel.setBorder(new EmptyBorder(10, 10, 10, 10));
        formPanel.add(new JLabel("Nuevo nombre:"));
        
        txtNuevoNombre = new JTextField(departamentoAEditar.getName(), 20); 
        formPanel.add(txtNuevoNombre);
        
        add(formPanel, BorderLayout.CENTER);

        JPanel buttonPanel = new JPanel(new FlowLayout(FlowLayout.RIGHT));
        JButton btnGuardar = new JButton("Guardar");
        JButton btnCancelar = new JButton("Cancelar");
        buttonPanel.add(btnGuardar);
        buttonPanel.add(btnCancelar);
        
        add(buttonPanel, BorderLayout.SOUTH);


        btnGuardar.addActionListener(e -> guardarCambios());
        btnCancelar.addActionListener(e -> dispose());
    }

    private void guardarCambios() {
        String nuevoNombre = txtNuevoNombre.getText().trim();
        
        if (nuevoNombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vac√≠o.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        departmentController.actualizarDepartamento(departamentoAEditar.getCode(), nuevoNombre);
        
        dispose();
    }

